package com.dbw.cfg;

public class DatabaseConfig {
    private String name;
    private String type;
    private String host;
    private int port;
    private String user;
    private String password;

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getHost() {
        return host;
    }

    public int getPort() {
        return port;
    }

    public String getUser() {
        return user;
    }

    public String getPassword() {
        return password;
    }
}